// Command gen renders the authoritative keyword table
// (internal/parsers/grammar/keywords_table.go) as a human-readable
// markdown reference at docs/annotation-keywords.md.
//
// It is invoked via `go generate ./internal/parsers/grammar/...` and
// produces output deterministically: the same input produces the same
// output, byte for byte. CI checks that running the generator leaves
// the committed file unchanged.
//
// Usage:
//
//	go run ./gen -out ../../../docs/annotation-keywords.md
package main

import (
	"bytes"
	"flag"
	"fmt"
	"io"
	"os"
	"path/filepath"
	"strings"

	"github.com/go-openapi/codescan/internal/parsers/grammar"
)

const (
	exitUsage = 2
	exitError = 1

	// Markdown output is committed to git and read by humans / CI; standard
	// file/dir perms. The file itself contains no secrets.
	fileMode = 0o644
	dirMode  = 0o755
)

func main() {
	out := flag.String("out", "", "output markdown file (required)")
	flag.Parse()

	if *out == "" {
		fmt.Fprintln(os.Stderr, "gen: -out is required")
		os.Exit(exitUsage)
	}

	var buf bytes.Buffer
	render(&buf, grammar.Keywords())

	if err := writeFile(*out, buf.Bytes()); err != nil {
		fmt.Fprintf(os.Stderr, "gen: %v\n", err)
		os.Exit(exitError)
	}
}

func writeFile(path string, data []byte) error {
	if err := os.MkdirAll(filepath.Dir(path), dirMode); err != nil {
		return err
	}
	return os.WriteFile(path, data, fileMode)
}

func render(w io.Writer, kws []grammar.Keyword) {
	fmt.Fprintln(w, "<!-- AUTO-GENERATED by internal/parsers/grammar/gen — DO NOT EDIT. -->")
	fmt.Fprintln(w, "<!-- Source: internal/parsers/grammar/keywords_table.go.")
	fmt.Fprintln(w, "     Regenerate with `go generate ./internal/parsers/grammar/...`. -->")
	fmt.Fprintln(w)
	fmt.Fprintln(w, "# Annotation keywords")
	fmt.Fprintln(w)
	fmt.Fprintf(w, "This document catalogs the %d `keyword: value` forms recognized by\n", len(kws))
	fmt.Fprintln(w, "the v2 grammar parser.")
	fmt.Fprintln(w)

	renderSummary(w, kws)
	renderDetails(w, kws)
}

func renderSummary(w io.Writer, kws []grammar.Keyword) {
	fmt.Fprintln(w, "## Summary")
	fmt.Fprintln(w)
	fmt.Fprintln(w, "| Keyword | Aliases | Value type | Legal in |")
	fmt.Fprintln(w, "|---------|---------|------------|----------|")
	for _, kw := range kws {
		fmt.Fprintf(w, "| `%s` | %s | `%s` | %s |\n",
			kw.Name,
			formatAliases(kw.Aliases),
			kw.Value.Type,
			formatContexts(kw.Contexts),
		)
	}
	fmt.Fprintln(w)
}

func renderDetails(w io.Writer, kws []grammar.Keyword) {
	fmt.Fprintln(w, "## Details")
	fmt.Fprintln(w)
	for _, kw := range kws {
		fmt.Fprintf(w, "### `%s`\n\n", kw.Name)

		if len(kw.Aliases) > 0 {
			fmt.Fprintf(w, "- **Aliases:** %s\n", formatAliases(kw.Aliases))
		}
		fmt.Fprintf(w, "- **Value type:** `%s`", kw.Value.Type)
		if len(kw.Value.Values) > 0 {
			fmt.Fprintf(w, " (one of: %s)", formatEnumValues(kw.Value.Values))
		}
		fmt.Fprintln(w)

		fmt.Fprintln(w, "- **Legal contexts:**")
		for _, ctx := range kw.Contexts {
			fmt.Fprintf(w, "  - `%s` — %s\n", ctx.Kind, ctx.Doc)
		}

		if kw.Doc != "" {
			fmt.Fprintf(w, "\n%s\n", kw.Doc)
		}
		fmt.Fprintln(w)
	}
}

func formatAliases(aliases []string) string {
	if len(aliases) == 0 {
		return "—"
	}
	out := make([]string, len(aliases))
	for i, a := range aliases {
		out[i] = "`" + a + "`"
	}
	return strings.Join(out, ", ")
}

func formatContexts(ctxs []grammar.ContextDoc) string {
	if len(ctxs) == 0 {
		return "—"
	}
	out := make([]string, len(ctxs))
	for i, c := range ctxs {
		out[i] = c.Kind.String()
	}
	return strings.Join(out, ", ")
}

func formatEnumValues(vs []string) string {
	out := make([]string, len(vs))
	for i, v := range vs {
		out[i] = "`" + v + "`"
	}
	return strings.Join(out, ", ")
}
